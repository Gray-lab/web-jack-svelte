	dialog {
		max-width: 32em;
		border: none;
		padding: 20px 40px;
		background-color: rgb(49, 49, 49);
	}
	dialog::backdrop {
		background: rgba(0, 0, 0, 0.5);
	}
	dialog > div {
		padding: 1em;
